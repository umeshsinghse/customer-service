package com.example.customer;

/**
 * Simple Customer model class.
 * Contains only basic fields: id, name, email.
 * No JPA annotations, no database mapping.
 */
public class Customer {
    private Long id;
    private String name;
    private String email;

    // Default constructor
    public Customer() {}

    // Constructor with all fields
    public Customer(Long id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Customer{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", email='" + email + '\'' +
                '}';
    }
}